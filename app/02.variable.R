# 変数について学びます。
# 「<-」を使用して変数に値を代入します。

a <- 1
b <- 2
c <- 3

print(a)
print(b)
print(c)

# [1] 1
# [1] 2
# [1] 3

# 変数名は「.」を含めることができます。
# 変数名は「.」で始めることができます。
# 変数名は「.」で終わることができます。
# 変数名は「.」のみであることができます。
# では、ドットを使用してみましょう♪

. <- "I am a dot"
print(.)

# [1] "I am a dot"

# それ以外は他の言語とほとんど同じです。
# ただし、R言語では、変数名に日本語を使用することができません。
