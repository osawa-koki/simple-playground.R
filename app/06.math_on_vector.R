# 次に配列の中の要素を一定の条件から検索しましょう♪

# 例えば、配列の中から、最大値と最小値を取得したい場合、
# 以下のように記述することができます。

n5 <- c(1, 2, 3, 4, 5)
print(max(n5))
print(min(n5))

# [1] 5
# [1] 1

# 特定の値の位置を取得したい場合、
# 以下のように記述することができます。

n6 <- c("a", "b", "c", "d", "e")
print(which(n6 == "c"))

# [1] 3

# match関数を使用することも可能です。

print(match("c", n6))

# 最大値・最小値の位置を取得したい場合、
# 以下のように記述することができます。

n7 <- c(3, -2, 5, 1, 0)
print(which.max(n7))
print(which.min(n7))

# [1] 3
# [1] 2
