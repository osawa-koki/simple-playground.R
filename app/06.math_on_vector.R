# 配列に対する演算を行います。
# R言語の最大の?特徴ともいえ、非常に簡単に配列に対する演算を行うことができます。
# 例えば、配列の要素をすべて2倍にしたい場合、
# 以下のように記述することができます。

n1 <- c(1, 2, 3, 4, 5)
print(n1 * 2)

# [1]  2  4  6  8 10

# また、配列の要素同士の演算も可能です。

n2 <- c(1, 2, 3, 4, 5)
print(n1 + n2)

# [1]  2  4  6  8 10

# また、配列の要素同士の比較も可能です。

n3 <- c(-2, 0, 6, 8, -5)
n4 <- c(1, 2, 3, 4, 5)
print(n3 < n4)

# [1] TRUE  TRUE FALSE FALSE  TRUE

